#include "libft.h"

int	ft_atoi(const char *str)
{
	int	res;
	int	mult;

	res = 0;
	mult = 1;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '+')
		str++;
	else if (*str == '-')
	{
		mult = mult * -1;
		str++;
	}
	while (*str >= 48 && *str <= 57)
	{
		res = (res * 10) + (*str - 48);
		str++;
	}
	return (res * mult);
}
